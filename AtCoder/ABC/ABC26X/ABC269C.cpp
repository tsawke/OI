ll N;
set < ll > exist;
void dfs(int dep = 0, ll cur = 0){
    if(dep > 60){
        if(exist.find(cur) == exist.end())exist.insert(cur);
        return;
    }
    dfs(dep + 1, cur);
    if(N & (1ll << dep))dfs(dep + 1, cur + (1ll << dep));
}

