template < typename T = int >
inline T read(void);

ll tot(0);
ll ans(0);
priority_queue < ll, vector < ll >, greater < ll > > cur;

int main(){
    int N = read(); ll L = read < ll >();
    for(int i = 1; i <= N; ++i){
        int v = read();
        cur.push(v), tot += v;
    }if(tot < L)cur.push(L - tot);
    while(cur.size() > 1){
        ll x = cur.top(); cur.pop();
        ll y = cur.top(); cur.pop();
        ans += (ll)x + y, cur.push((ll)x + y);
    }printf("%lld\n", ans);
    fprintf(stderr, "Time: %.6lf\n", (double)clock() / CLOCKS_PER_SEC);
    return 0;
}

template < typename T >
inline T read(void){
    T ret(0);
    int flag(1);
    char c = getchar();
    while(c != '-' && !isdigit(c))c = getchar();
    if(c == '-')flag = -1, c = getchar();
    while(isdigit(c)){
        ret *= 10;
        ret += int(c - '0');
        c = getchar();
    }
    ret *= flag;
    return ret;
}